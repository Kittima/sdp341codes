# -*- coding: utf-8 -*-
# We can rotate the turtle in order to draw more interesting figures.
import turtle

silly = turtle.Turtle()

silly.forward(50)   # Move the turtle forward in the direction its facing by 50 pixels
silly.right(90)     # Rotate clockwise by 90 degrees

silly.forward(50)
silly.right(90)

silly.forward(50)
silly.right(90)

silly.forward(50)
silly.right(90)

turtle.done()